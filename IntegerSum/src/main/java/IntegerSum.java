    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{//try catch statement to handle exceptions
         do{ // do while loop to handle increase array size with added values
            arr.add(Integer.parseInt(inInt.getText())); // add input to arr
            size ++; // add 1 to size
         }while(size < arr.size()); // continue loop while size is less than array size
         inInt.setText(""); // emptiy input field
     }catch(NumberFormatException e){ //if invalid entry, show error message
         JOptionPane.showMessageDialog(null, "Error, please enter an integer.");
     }   
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(inInt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Error, please enter a value to remove.");// if user has not inputted value, prompt user to enter a value
        }else if(arr.isEmpty()){ // if the arr has no values
            JOptionPane.showMessageDialog(null, "List is empty, please add numbers.");//prompts user to add to arr
        }else{
            // for loop to find inputted variable
            for(int i = 0; i < arr.size(); i++){
              if(arr.get(i) == Integer.parseInt(inInt.getText())){ // check if any values = to one entered
              arr.remove(i); // if so, removes value
              size--; // subtract 1 from size if value is removed
              }
            }
         }
        inInt.setText(""); //empty input field
        displayArr(); // executes displays arr
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListActionPerformed
        if(arr.isEmpty()){//if the array is empty, run code
            JOptionPane.showMessageDialog(null, "Please add numbers to the list first.");//prompts user to add to list
        }else{
            txtDisplay1.setText(""); // empty txtDisplay1
            int chosenlist = JOptionPane.showOptionDialog(null, "What would you like to see from the list?", "List Type", 0, JOptionPane.INFORMATION_MESSAGE, null, options, null);//Creates int chosenlist to obtain information on option chosen to list from option string[]
            listOptions(chosenlist);
        } 
    }//GEN-LAST:event_btnListActionPerformed

    private void btnSumAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumAllActionPerformed
        for(int i : arr){//for loop to go through each value
            sum += i; // added to cumulative sum
        }
        txtDisplay2.setText("The sum of all values is "+String.valueOf(sum)); // converts the sum into a string and displays on txtDisplay2
        sum = 0; // reset sum
    }//GEN-LAST:event_btnSumAllActionPerformed

    private void btnSumEvenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumEvenActionPerformed
        for(int i = 0; i < arr.size(); i++){//for loop to go through each value
            if(arr.get(i) %2 == 0){ // obtain even values
                sum += arr.get(i); // added to cumulative sum
                txtDisplay2.setText("The sum of all even values is "+String.valueOf(sum)); // converts sum into string and displays
            }else{
                txtDisplay2.setText("No even values.");//if no even, state
            }
        }
        sum = 0; // resest sum
    }//GEN-LAST:event_btnSumEvenActionPerformed

    private void btnSumOddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumOddActionPerformed
        for(int i = 0; i < arr.size(); i++){//for looop to go throug values
            if(arr.get(i) %2 != 0){ // obtain odd values
                sum += arr.get(i); // added to sum
                txtDisplay2.setText("The sum of all odd numbers is "+String.valueOf(sum)); // converts sum into string and displays
            }else{
                txtDisplay2.setText("No odd values.");//if no odd, state
            }
        }
        sum = 0; // reset sum
    }//GEN-LAST:event_btnSumOddActionPerformed

