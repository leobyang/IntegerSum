    /*
     *@desc FUnction to display array
     */
    private void displayArr(){
        txtDisplay1.setText(""); // Empties txtDisplay1 in case of prior output
        for(int i = 0; i < arr.size(); i++){ //for loop to go get values
            txtDisplay1.setText(txtDisplay1.getText() + "\n" + arr.get(i).toString()); // displays array values
        }
    }
    
    /*
     *@desc Function to handle the different list options (even, odd, all). 
     */
    private void listOptions(int option){
        switch(option){
            case 0: // case for list even
                for(int i = 0; i < arr.size(); i++){ //for loop to find even numbers
                    if(arr.get(i) %2 == 0){ // If array element is even (remainder when divided by 2 = 0)
                        txtDisplay1.setText(txtDisplay1.getText() + "\n" + arr.get(i).toString()); // displays even numbers
                 }
                }
                break;
            case 1: // case for list even
                for(int i = 0; i < arr.size(); i++){ //for loop to find odd numbers
                    if(arr.get(i) %2 != 0){ // If array element is odd (remainder when divided by 2 != 0)
                        txtDisplay1.setText(txtDisplay1.getText() + "\n" + arr.get(i).toString()); //display odd
                 }
                }
                break;
            case 2: // case for list all
                displayArr(); // executes displayArr()
                break;
        }
    }

